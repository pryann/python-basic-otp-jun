# Static type vs dynamic type

# Static: data type checked before exectuion
# Dynamic: data type checked during exectuion

# python type checked during exec.
age = 12
print(age)

# Explicit vs implicit (erősen vagy gyengén típusos)
# Explicit: use see the type in the source code: int age = 12
# Implicit: use can not  see the type in the source code: age = 12

# implicit
age = 12
print(age)

# explicit
age = int(12.123)
